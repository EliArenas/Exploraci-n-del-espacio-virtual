//Definición clásica o tradicional
//de una clase
public class MyClass
{
    //Attributos son todas las
    //variables que tiene en su
    //poder esta clase
    public int _intValorA;
    public int _intValorB;

    //Métodos son las estructuras
    //funcionales de una clase
    //para que la clase opere por
    //nosotros

    //Este método es público, ya que
    //cualquier otra clase puede acceder
    //a este método y así también
    //en vez de void, regresa un número
    //entero después de la suma
    public int Suma()
    {
        return _intValorA + _intValorB;
    }
}
